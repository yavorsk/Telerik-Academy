//8. Write an expression that calculates trapezoid's area by given sides a and b and height h.

var sideA = 2;
var sideB = 5;
var height = 3;

var area = (sideA + sideB) * height / 2;

jsConsole.writeLine("Side A = " + sideA);
jsConsole.writeLine("Side B = " + sideB);
jsConsole.writeLine("Height = " + height);
jsConsole.writeLine("Area = " + area);